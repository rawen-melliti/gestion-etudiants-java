    private void jButtonajouMMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonajouMMouseClicked
        String nom = jTextnommat.getText().trim();
    String coefText = jTextcoef.getText().trim();

    //Vérifier champs vides
    if(nom.equals("") || coefText.equals("")){
        JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs");
        return;
    }

    // Vérifier nom 
    if(!nom.matches("[a-zA-Z ]+")){
        JOptionPane.showMessageDialog(this, "Nom matière invalide !");
        return;
    }

    //Vérifier coef
    float coef;
    try {
        coef = Float.parseFloat(coefText);

        if(coef <= 0 || coef >+20){
            JOptionPane.showMessageDialog(this, "Coef entre 1 et 5");
            return;
        }

    } catch(NumberFormatException e){
        JOptionPane.showMessageDialog(this, "Coef doit être un nombre !");
        return;
    }

    //  Vérifier doublon matière
    DefaultTableModel model = (DefaultTableModel) jTablemat.getModel();

    for(int i = 0; i < model.getRowCount(); i++){
        String exist = model.getValueAt(i, 0).toString();
        if(exist.equalsIgnoreCase(nom)){
            JOptionPane.showMessageDialog(this, "Matière déjà existe!");
            return;
        }
    }

    // Ajouter dans table
    model.addRow(new Object[]{nom, coef});

    // Ajouter dans liste
    String matiere = nom + " " + coef;
    Note.addMatiere(matiere);

    // vider champs
    jTextnommat.setText("");
    jTextcoef.setText("");
    }//GEN-LAST:event_jButtonajouMMouseClicked

