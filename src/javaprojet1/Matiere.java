    private void jButtonsuivmatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonsuivmatMouseClicked
        try {
            // 1. On récupère les modèles de données des tables déjà ouvertes
            // Remplace 'jTableetudiant' et 'jTablemat' par tes vrais noms de variables
            DefaultTableModel etuModel = (DefaultTableModel) Etudiant.jTableetudiant.getModel();
            DefaultTableModel matModel = (DefaultTableModel) Matiere.jTablemat.getModel();

            // 2. On crée la fenêtre Note en lui DONNANT ces modèles
            Note n = new Note(etuModel, matModel);

            // 3. On affiche la fenêtre
            n.setVisible(true);

        } catch (Exception e) {
            // Si les fenêtres Etudiant ou Matiere ne sont pas encore ouvertes,
            // on crée des modèles vides pour éviter que le programme plante.
            DefaultTableModel videE = new DefaultTableModel();
            DefaultTableModel videM = new DefaultTableModel();
            new Note(videE, videM).setVisible(true);
        }
    }//GEN-LAST:event_jButtonsuivmatMouseClicked

