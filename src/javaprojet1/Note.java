    private void jButtonajouNMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonajouNMouseClicked
        //!                                          
    String etudiant = jComboBoxetudiantN.getSelectedItem() != null ? jComboBoxetudiantN.getSelectedItem().toString() : "";
    String matiere = jComboBoxmatN.getSelectedItem() != null ? jComboBoxmatN.getSelectedItem().toString() : "";
    String note = jTextFieldnoteN.getText();

    if(!etudiant.equals("") && !matiere.equals("") && !note.equals("")) {
        double n;
         //vérifier note (numérique)
    try {
        n=Double.parseDouble(note); // vérifie note
    } catch(NumberFormatException e){
        JOptionPane.showMessageDialog(this, "La note doit être un nombre !");
        return;
    }

    //vérifier intervalle note
    if(n < 0 || n > 20){
        JOptionPane.showMessageDialog(this, "Note entre 0 et 20 !");
        return;
    }

    DefaultTableModel model = (DefaultTableModel) jTablenote.getModel();
    // ajout 
    model.addRow(new Object[]{etudiant, matiere, note});

    //vider champ
    jTextFieldnoteN.setText("");
    }
    }//GEN-LAST:event_jButtonajouNMouseClicked

