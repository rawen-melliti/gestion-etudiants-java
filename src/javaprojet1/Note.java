public void appliquerLeTheme() {
    // 1. On récupère les couleurs statiques depuis la classe Parametre
    java.awt.Color fond = Parametre.isDark ? Parametre.bgDark : Parametre.monFondOriginal;
    java.awt.Color texte = Parametre.isDark ? Parametre.fgDark : Parametre.monTexteOriginal;

    // 2. On applique au conteneur principal (le panel qui couvre tout)
    jPanel1.setBackground(fond);
    jPanel2.setBackground(fond);

    // 3. On applique à TOUS les labels, boutons et radios de CETTE interface
    // C'est cette partie qui change selon le nom de tes composants
    jLabeletudianN.setForeground(texte); 
    jLabelmatN.setForeground(texte);
    jLabelnoteN.setForeground(texte);
    jLabel1.setForeground(texte);
    
    
    
    // 4. On force le rafraîchissement visuel
    this.repaint();
}
      
    

