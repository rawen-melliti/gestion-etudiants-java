    public Note(DefaultTableModel modeleEtu, DefaultTableModel modeleMat) {
        initComponents();
        appliquerLeTheme();
        
        // Remplir la liste des étudiants avec les données reçues
        jComboBoxetudiantN.removeAllItems();
        jComboBoxetudiantN.addItem("");
        if (modeleEtu != null) {
            for (int i = 0; i < modeleEtu.getRowCount(); i++) {
                String nom = modeleEtu.getValueAt(i, 0).toString();
                String prenom = modeleEtu.getValueAt(i, 1).toString();
                jComboBoxetudiantN.addItem(nom + " " + prenom);
            }
        }

        // Remplir la liste des matières avec les données reçues
        jComboBoxmatN.removeAllItems();
        jComboBoxmatN.addItem("");
        if (modeleMat != null) {
            for (int i = 0; i < modeleMat.getRowCount(); i++) {
                jComboBoxmatN.addItem(modeleMat.getValueAt(i, 0).toString());
            }
        }
        
        // Configuration du MouseListener pour la modification
        jTablenote.addMouseListener(new java.awt.event.MouseAdapter() {
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int selected = jTablenote.getSelectedRow();
                if (selected != -1) {
                    jComboBoxetudiantN.setSelectedItem(jTablenote.getValueAt(selected, 0).toString());
                    jComboBoxmatN.setSelectedItem(jTablenote.getValueAt(selected, 1).toString());
                    jTextFieldnoteN.setText(jTablenote.getValueAt(selected, 2).toString());
                }
            }
        });

    }
